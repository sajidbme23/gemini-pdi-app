# --- 🌟 MODERN PDF TABLE FUNCTION ---
def create_pdf_table(df):
    # Landscape orientation (L) taaki columns achhe se khul kar aayein
    pdf = FPDF(orientation="L") 
    pdf.add_page()
    
    # --- Modern Header Setup ---
    # Helvetica ek clean, Calibri jaisa modern font hai
    pdf.set_font("Helvetica", 'B', 18) 
    pdf.set_text_color(0, 51, 102) # Dark Blue Color for Professional Look
    pdf.cell(0, 10, txt="HITES PDI Technical Report", align='C', ln=1)
    
    pdf.set_font("Helvetica", size=11)
    pdf.set_text_color(100, 100, 100) # Grey color for subtitle
    pdf.cell(0, 8, txt="Generated by: Mohammad Sajid (Biomedical Engineer Grade 1)", align='C', ln=1)
    pdf.ln(5)
    
    # --- Table Styling ---
    pdf.set_text_color(0, 0, 0) # Wapas Black color table ke liye
    
    # Column width ka ratio set karna (Total 100% man lein)
    # S.No (8%), English (40%), Hindi (30%), Inspection Check (22%)
    col_widths_ratio = (8, 40, 30, 22)
    
    with pdf.table(
        col_widths=col_widths_ratio,
        text_align=("CENTER", "LEFT", "LEFT", "LEFT"),
        line_height=7, # Padding/Line spacing badha di taaki khula-khula lage
        borders_layout="ALL"
    ) as table:
        
        # Table Header Row (Bold)
        header_row = table.row()
        pdf.set_font("Helvetica", 'B', 11) # Header ka font thoda bada aur bold
        for col in df.columns:
            header_row.cell(str(col))
            
        # Table Data Rows (Regular)
        pdf.set_font("Helvetica", '', 10) # Data ka font clean aur padhne me aasan
        for _, data_row in df.iterrows():
            row = table.row()
            for item in data_row:
                # Text ko clean kar rahe hain taaki Latin-1 error na aaye
                clean_text = str(item).encode('latin-1', 'replace').decode('latin-1')
                row.cell(clean_text)
                
    return bytes(pdf.output())

