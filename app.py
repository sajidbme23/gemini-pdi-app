# --- 🌟 ENHANCED PDF TABLE FUNCTION (Handles Large Data) ---
def create_pdf_table(df):
    # Landscape orientation, A4 format
    pdf = FPDF(orientation="L", unit="mm", format="A4") 
    pdf.set_auto_page_break(auto=True, margin=15)
    pdf.add_page()
    
    # --- Modern Header Setup ---
    pdf.set_font("Helvetica", 'B', 16) 
    pdf.set_text_color(0, 51, 102) # Dark Blue
    pdf.cell(0, 8, txt="HITES PDI Technical Report (Detailed)", align='C', ln=1)
    
    pdf.set_font("Helvetica", size=10)
    pdf.set_text_color(100, 100, 100) # Grey
    pdf.cell(0, 6, txt="Generated by: Mohammad Sajid (Biomedical Engineer Grade 1)", align='C', ln=1)
    pdf.ln(5)
    
    # --- Table Styling ---
    pdf.set_text_color(0, 0, 0)
    
    # S.No (5%), English (27%), Hindi (28%), Inspection Guide (40%)
    col_widths_ratio = (5, 27, 28, 40)
    
    with pdf.table(
        col_widths=col_widths_ratio,
        text_align=("CENTER", "LEFT", "LEFT", "LEFT"),
        line_height=7, 
        borders_layout="ALL"
    ) as table:
        
        # Table Header Row (Repeats on every page automatically)
        header_row = table.row()
        pdf.set_font("Helvetica", 'B', 10)
        for col in df.columns:
            header_row.cell(str(col))
            
        # Table Data Rows
        pdf.set_font("Helvetica", '', 9)
        for _, data_row in df.iterrows():
            row = table.row()
            for item in data_row:
                # Cleaning characters for FPDF
                clean_text = str(item).encode('latin-1', 'replace').decode('latin-1')
                row.cell(clean_text)
                
    return bytes(pdf.output())

