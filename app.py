# --- PDF GENERATION FUNCTION (PERFECT TABLE FORMAT) ---
def create_pdf_table(df):
    # Landscape orientation taaki 4 columns aaram se fit ho jayein
    pdf = FPDF(orientation="L") 
    pdf.add_page()
    
    # Title aur Header
    pdf.set_font("Helvetica", 'B', 16)
    pdf.cell(0, 10, txt="HITES PDI Technical Report", align='C', ln=1)
    pdf.set_font("Helvetica", size=10)
    pdf.cell(0, 10, txt="Generated by: Mohammad Sajid (Biomedical Engineer)", align='C', ln=1)
    pdf.ln(5)
    
    # Table ki setting
    pdf.set_font("Helvetica", size=9)
    
    # FPDF2 ka table feature use kar rahe hain
    with pdf.table(text_align="LEFT") as table:
        # Header Row
        header_row = table.row()
        for col in df.columns:
            header_row.cell(str(col))
            
        # Data Rows
        for _, data_row in df.iterrows():
            row = table.row()
            for item in data_row:
                # Text ko clean kar rahe hain taaki Latin-1 error na aaye
                clean_text = str(item).encode('latin-1', 'replace').decode('latin-1')
                row.cell(clean_text)
                
    return bytes(pdf.output())

